    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        try{
            con = Koneksi.getKoneksi();
            String sql = "UPDATE `mahasiswa_new` SET `npm` = '"+npm.getText()+"', `nama` = '"+nama.getText()+"', `prodi` = '"+prodi.getText()+"', `fakultas` = '"+fakultas.getText()+"' WHERE `mahasiswa_new`.`id` = "+this.id+"";

            st = con.createStatement();
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Data buku berhasil diubah");
            new DataMahasiswa().setVisible(true);
            this.dispose();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

